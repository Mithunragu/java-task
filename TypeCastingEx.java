class TypeCastingEx{


public static void main(String args[]){

	int a = 5;
	double b = a;//Automatic Casting also know as Widening	

	System.out.println(a);	
	System.out.println(b);	

 double c = 10.5d;
 int d = (int) c; //Manual Casting
	System.out.println(c);	
	System.out.println(d);	








}
}